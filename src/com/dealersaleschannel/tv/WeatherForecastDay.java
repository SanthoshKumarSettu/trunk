package com.dealersaleschannel.tv;

public class WeatherForecastDay {

	private String day;
	private String weather;
	private String weatherImage;
	private String highTemperature;
	private String lowTemperature;
	private String percentOfPrecipitation;
	
	
	public String getDay() {		
		return this.day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public String getWeatherImage() {
		return this.weatherImage;
	}
	public void setWeatherImage(String weatherImage) {
		this.weatherImage = weatherImage;
	}
	public String getHighTemperature() {
		return this.highTemperature;
	}
	public void setHighTemperature(String highTemperature) {
		this.highTemperature = highTemperature;
	}
	public String getLowTemperature() {
		return this.lowTemperature;
	}
	public void setLowTemperature(String lowTemperature) {
		this.lowTemperature = lowTemperature;
	}
	public String getPercentOfPrecipitation() {
		return this.percentOfPrecipitation;
	}
	public void setPercentOfPrecipitation(String percentOfPrecipitation) {
		this.percentOfPrecipitation = percentOfPrecipitation;
	}
	
}
